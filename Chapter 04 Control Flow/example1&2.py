def practice1(x):
    """if, elif, else"""
    if x < 0:
        print('negative')
    elif x == 0:
        print('zero')
    else:
        print('non-negative')

def practice2(num):
    """if, else"""
    if num:
        print('TRUE')
    else:
        print('FALSE')
